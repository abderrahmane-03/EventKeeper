/**
 * The EventType enum represents different types of events in the application.
 *
 * Package: org.example.enums
 */
package org.example.enums;

/**
 * Enum EventType defines various types of events that can be organized
 * within the application. The available event types are:
 *
 * - CONFERENCE: A formal meeting for discussion, often with multiple speakers or topics.
 * - WORKSHOP: A hands-on training or educational session, typically focused on skill development.
 * - SEMINAR: A small group discussion or lecture on a particular topic.
 * - WEBINAR: A seminar or presentation conducted over the internet.
 */
public enum EventType {
    CONFERENCE,
    WORKSHOP,
    SEMINAR,
    WEBINAR;
}
