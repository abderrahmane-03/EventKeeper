/**
 * The UserRole enum represents the different roles a user can have in the application.
 *
 * Package: org.example.enums
 */
package org.example.enums;

/**
 * Enum UserRole defines the roles that users can assume within the application.
 * The available roles are:
 *
 * - ADMIN: A user with administrative privileges, typically responsible for managing the application or events.
 * - PARTICIPANT: A user who participates in events but does not have administrative control.
 */
public enum UserRole {
    ADMIN,
    PARTICIPANT
}
